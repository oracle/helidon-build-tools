package io.helidon.lsp.server.model;

import java.util.List;

/**
 * Model to represent the hint attribute in the helidon-configuration-metadata.json .
 */
public class ConfigurationHint {

    private String name;
    private List<Value> values;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<Value> getValues() {
        return values;
    }

    public void setValues(List<Value> values) {
        this.values = values;
    }

    /**
     * Model to represent the value attribute in the Hint attribute in the helidon-configuration-metadata.json .
     */
    public static class Value {
        private String value;
        private String description;

        public String getValue() {
            return value;
        }

        public void setValue(String value) {
            this.value = value;
        }

        public String getDescription() {
            return description;
        }

        public void setDescription(String description) {
            this.description = description;
        }
    }
}
