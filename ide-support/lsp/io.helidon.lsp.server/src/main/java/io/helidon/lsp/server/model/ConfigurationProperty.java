package io.helidon.lsp.server.model;

/**
 * Model to represent the properties attribute in the helidon-configuration-metadata.json .
 */
public class ConfigurationProperty {

    private String name;
    private String type;
    private String description;
    //TODO change type to Object for arrays, change this field in config json in helidon jar
    // (look as an example the config file in test resource), test changes
    private String defaultValue;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDefaultValue() {
        return defaultValue;
    }

    public void setDefaultValue(String defaultValue) {
        this.defaultValue = defaultValue;
    }
}
